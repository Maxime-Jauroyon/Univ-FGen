package com.ustudents.fgen.maths;

/** Defines values within a polynomial complex. */
public class PolynomialFunctionValues {
    /** The complex z. */
    public Complex z;

    /** The complex c. */
    public Complex c;

    public PolynomialFunctionValues(Complex z, Complex c) {
        this.z = z;
        this.c = c;
    }
}
